# Execute a command as another user
# usage: exec_as USER COMMAND [ARG ...]
exec_as() {
	local user=$1
	shift 1

	if [[ $user = $(whoami) ]]; then
		eval "$@"
	else
		sudo --login --user="$user" "$@"
	fi
}

